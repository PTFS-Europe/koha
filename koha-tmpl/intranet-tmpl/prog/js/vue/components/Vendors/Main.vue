                        <Dialog />
                        <router-view />
                    </main>
                </div>

                <div class="col-md-2 order-sm-2 order-md-1">
                    <LeftMenu :title="$__('Acquisitions')"></LeftMenu>
                </div>
            </div>
        </div>
    </div>
    <div class="main container-fluid" v-else>
        <Dialog />
    </div>
</template>

<script>
import { inject } from "vue"
import Breadcrumbs from "../Breadcrumbs.vue"
import Help from "../Help.vue"
import LeftMenu from "../LeftMenu.vue"
import Dialog from "../Dialog.vue"
import "vue-select/dist/vue-select.css"
import { storeToRefs } from "pinia"
import { APIClient } from "../../fetch/api-client"

export default {
    setup() {
        const vendorStore = inject("vendorStore")
        const { config, user, currencies, libraryGroups, gstValues } =
            storeToRefs(vendorStore)
        const { setLibraryGroups } = vendorStore

        const mainStore = inject("mainStore")
        const { loading, loaded, setError } = mainStore
        const AVStore = inject("AVStore")

        const permissionsStore = inject("permissionsStore")
        const { userPermissions } = storeToRefs(permissionsStore)

        return {
            vendorStore,
            setError,
            loading,
            loaded,
            userPermissions,
            AVStore,
            config,
            user,
            currencies,
            libraryGroups,
            gstValues,
            setLibraryGroups,
        }
    },
    beforeCreate() {
        this.loading()

        const fetchConfig = () => {
            let promises = []

            const libraryClient = APIClient.libraries
            libraryClient.libraryGroups.getAll().then(
                libraryGroups => {
                    this.setLibraryGroups(libraryGroups)
                },
                error => {}
            )

            const av_client = APIClient.authorised_values
            const authorised_values = {
                av_vendor_types: "VENDOR_TYPE",
                av_vendor_interface_types: "VENDOR_INTERFACE_TYPE",
                av_vendor_payment_methods: "VENDOR_PAYMENT_METHOD",
                av_lang: "LANG",
            }

            let av_cat_array = Object.keys(authorised_values).map(function (
                av_cat
            ) {
                return '"' + authorised_values[av_cat] + '"'
            })

            promises.push(
                av_client.values
                    .getCategoriesWithValues(av_cat_array)
                    .then(av_categories => {
                        Object.entries(authorised_values).forEach(
                            ([av_var, av_cat]) => {
                                const av_match = av_categories.find(
                                    element => element.category_name == av_cat
                                )
                                this.AVStore[av_var] =
                                    av_match.authorised_values
                            }
                        )
                    })
            )

            return Promise.all(promises)
        }

        fetchConfig().then(() => {
            this.loaded()
            this.userPermissions = userPermissions
            this.config.settings.edifact = edifact
            this.config.settings.ermModule = ermModule
            this.currencies = currencies
            // this.libraryGroups = libraryGroups
            this.user.loggedInUser = loggedInUser
            this.user.loggedInUser.loggedInBranch = loggedInBranch.branchcode
            this.gstValues = gstValues.map(gv => {
                return {
                    label: `${(gv.option * 100).toFixed(2)}%`,
                    value: gv.option,
                }
            })
            this.initialized = true
        })
    },
    data() {
        return {
            initialized: false,
        }
    },
    methods: {},
    components: {
        Breadcrumbs,
        Dialog,
        Help,
        LeftMenu,
    },
}
</script>


<template>
    <div class="widget-details" :id="id">
        <div class="d-flex align-items-center widget-header">
            <h2 class="widget-name me-auto m-0">
                {{ name }}
            </h2>
            <div class="widget-actions">
                <button
                    v-if="!alreadyAdded"
                    class="btn btn-primary me-1 add-widget"
                    @click="addWidget"
                >
                    <font-awesome-icon icon="plus" />
                    {{ $__("Add") }}
                </button>
                <button
                    v-if="alreadyAdded"
                    class="btn btn-default remove-widget"
                    @click="removeWidget"
                >
                    <font-awesome-icon icon="trash" />
                    {{ $__("Remove") }}
                </button>
            </div>
        </div>
        <p class="widget-description">
            {{ description }}
        </p>
    </div>
</template>

<script>
export default {
    props: {
        id: {
            type: String,
            required: true,
        },
        name: {
            type: String,
            required: true,
        },
        description: {
            type: String,
            required: true,
        },
        alreadyAdded: {
            type: Boolean,
            required: false,
            default: false,
        },
    },
    methods: {
        removeWidget() {
            this.$emit("removed", this);
        },
        addWidget() {
            this.$emit("added", this);
        },
    },
    emits: ["removed", "added"],
};
</script>
<style scoped>
.widget-description {
    padding: 12px;
}

.widget-header {
    background-color: #f7f7f7;
    border-bottom: 1px solid #ccc;
    border-top-left-radius: 8px;
    border-top-right-radius: 8px;
    padding: 12px;
}

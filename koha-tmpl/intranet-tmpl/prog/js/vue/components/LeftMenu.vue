<template>
    <aside v-if="leftNavigation !== 'none'">
        <div id="navmenu">
            <div id="navmenulist">
                <VendorMenu v-if="leftNavigation === 'VendorMenu'" />
                <AcquisitionsMenu
                    v-else-if="leftNavigation === 'AcquisitionsMenu'"
                />
                <template v-else>
                    <h5>{{ $__(title) }}</h5>
                    <ul>
                        <NavigationItem
                            v-for="(item, key) in leftNavigation"
                            v-bind:key="key"
                            :item="item"
                        ></NavigationItem>
                    </ul>
                </template>
            </div>
        </div>
    </aside>
</template>

<script>
import { inject } from "vue";
import NavigationItem from "./NavigationItem.vue";
import VendorMenu from "./Islands/VendorMenu.vue";
import { storeToRefs } from "pinia";
import AcquisitionsMenu from "./Islands/AcquisitionsMenu.vue";

export default {
    name: "LeftMenu",
    setup: () => {
        const navigationStore = inject("navigationStore");
        const { leftNavigation } = storeToRefs(navigationStore);
        return {
            leftNavigation,
        };
    },
    async beforeMount() {
        if (this.condition) this.condition(this.leftNavigation);
    },
    props: {
        title: String,
        condition: Function,
    },
    components: {
        NavigationItem,
        VendorMenu,
        AcquisitionsMenu,
    },
};
</script>

